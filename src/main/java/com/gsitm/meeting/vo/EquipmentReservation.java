public class EquipmentReservation {
	
	private String resId;
	private String eqId;
	
	public EquipmentReservation() {
		super();
	}

	public String getResId() {
		return resId;
	}

	public void setResId(String resId) {
		this.resId = resId;
	}

	public String getEqId() {
		return eqId;
	}

	public void setEqId(String eqId) {
		this.eqId = eqId;
	}

}
